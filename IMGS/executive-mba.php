<p>The EMBA programme enables working executives to upgrade their managerial knowledge and skills while continuing to be employed. This rigorous programme is conducted through regular classes on weekends. The programme consists of a compulsory set of core subjects and a variety of elective subjects. The participants bring in their wide experiences into the classroom and work on regular individual and group projects. Throughout the programme, participants practice the concepts learnt in classrooms at their workplace. This parallel learning and implementation process form the basis of this programme.</p>

<H4>Why EMBA Programme at VGSoM ?</H4>



<UL>
	<LI>•	To fast-track the career of senior and mid-career executives</LI>
	<LI>•	To achieve leadership orientation for taking organisations to the next level</LI>
	<LI>•	For networking opportunities through IIT’s extensive alumni network</LI>

</UL>

<H4>Benefits and impact on your career</H4>

<p>VGSoM's Executive MBA sets you apart and gives you a genuine edge. Whether your goal is to progress within your organization, take on new or global responsibilities, or pursue an entrepreneurial route, the programme will give you the knowledge, insights and confidence to operate successfully anywhere in the world.</p>

<p>Studying while you remain in full-time employment enables you to improve your workplace performance from day one as you apply each new classroom concept with direct relevance to you and your organization. The practical business expertise and the new ways of management thinking that you develop at the School change your approach to decision making and give you the ideas, inspiration and contacts to shape your future career.</p>


<UL>
	<LI>•	You are able to move up in your career quickly and with confidence.</LI>
	<LI>•	You can successfully make the transition to general management from a technical or functional role.</LI>
	<LI>•	The elective portfolio allows you to specialize in certain subject areas or gain breadth across a range of business and management disciplines.</LI>
	<LI>•	You gain entrepreneurial skills to help you develop your own venture, or to implement new and innovative ideas within your organization.</LI>
	<LI>•	You are able to build a strong network of personal and professional contacts.</LI>
	<LI>•	You continue to benefit from the School and IIT's reputation, recognition and global networks well beyond your time in the programme</LI>

</UL>




